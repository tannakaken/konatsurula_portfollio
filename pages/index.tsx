type Work = {
  id: string,
  title: string;
  youtubeId: string;
  description: string;
}

type News = {
  id: string;
  title: string;
  content: string;
  isNew: boolean;
}

type ImageData = {
  url: string;
  height: number;
  width: number;
}

type Illust = {
  id: string;
  title: string;
  image: ImageData;
  description: string;
}

interface Params extends ParsedUrlQuery {
}

type Props = {
  works: Work[];
  news: News[];
  illusts: Illust[];
}

export const getStaticProps: GetStaticProps<Props, Params> = async (context) => {
  const client = createClient({
    serviceDomain: "konatsuruka",
    apiKey: "c13f9e56bac0488f8c4dd33995f60b6f8488",
  });
  const works = (await client.get<{contents: Work[]}>({endpoint: "works"})).contents;
  const news = (await client.get<{contents: News[]}>({endpoint: "news"})).contents
  const illusts = (await client.get<{contents: Illust[]}>({endpoint: "illusts"})).contents

  return { props: {
      works,
      news,
      illusts,
    }
  }
};

